@Composable
fun PruebasTFGTheme(
    viewModel: AppViewModel,
    content: @Composable () -> Unit
) {
    val isThemeDark = viewModel.isThemeDark.collectAsState(initial = true)

    val colors = if (isThemeDark.value == true) DarkColorScheme else LightColorScheme

    MaterialTheme(
        colorScheme = colors,
        typography = Typography,
        content = content
    )
}